package ascii_art;
/**
 * The OutputOption enum represents the output options of the program.
 * The OutputOption enum has the following values:
 * HTML - the output is written in a HTML file.
 * CONSOLE - the output is written in the console.
 */
public enum OutputOption {
    /**
     * The output is written in a HTML file.
     */
    HTML,
    /**
     * The output is written in the console.
     */
    CONSOLE
}
